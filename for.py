for i in range(8): # range(8) > fino a 8 (escluso)
    print("i:", i)

for i in range(1,8): # range(1,8) > da 1 a 8 (escluso)
    print("i:", i)

for i in range(1,8,2): # range(1,8,2) > da 1 a 8 con salti di 2
    print("i:", i)